import UIKit

extension UIColor{
    
    open class var mainOrange: UIColor{
        return UIColor(named: "MainOrange")!
    }
    open class var shadeOrange: UIColor{
        return UIColor(named: "ShadeOrange")!
    }
    open class var mainGray: UIColor{
        return UIColor(named: "GrayColor")!
    }
    
}
